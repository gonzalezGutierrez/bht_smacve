                <h3 class="text-white">¿Qué es la Angiología?</h3>
            </div>
            <div class="row">
                <div class="col-sm-12 text-white font-size16 font-weight-300 text-justify">
                    <p>La Angiología, Cirugía Vascular y Endovascular es la especialidad médico-quirúrgica responsable del diagnóstico y tratamiento de las enfermedades del aparato circulatorio, una extensa red de ‘tuberías' cuya misión consiste en hacer llegar la sangre, y con ella el oxígeno y los nutrientes a todas las células de nuestro organismo y devolverla de nuevo al corazón.</p>
                    <p>Las arterias llevan la sangre desde el corazón hasta las células de todos los rincones de nuestro cuerpo y las venas la devuelven de nuevo hacia el corazón, donde se inicia un nuevo ciclo. No debemos olvidar los vasos linfáticos, cuya misión es algo diferente, pero en íntima relación con los sistemas arterial y venoso.</p>
                    <p>Todas estas "tuberías" son estructuras vivas y sus enfermedades repercuten directamente en el órgano al que llevan el aporte sanguíneo e incluso en todo el organismo. El infarto de miocardio, la embolia cerebral y el tromboembolismo pulmonar son algunas de las principales causas de muerte de nuestra civilización, y todas ellas son debidas a un problema vascular.</p>
                    <p>La más frecuente de las enfermedades del sistema venoso son las <strong>varices de las piernas</strong>, enfermedad exclusiva de la raza humana, desde el momento que nuestros antepasados decidieron erguirse y apoyarse sólo en las extremidades inferiores y una de las patologías más frecuentes en nuestro medio. Puede ser sólo un pequeño problema estético, pero pueden comportar también serias complicaciones: flebitis, trombosis, úlceras en las piernas. embolias pulmonares., etc, que pueden evitarse con un tratamiento a tiempo.</p>
                    <p>El sistema arterial es vital. Sin el suficiente aporte de sangre, y con ella de oxígeno y nutrientes, ningún órgano de nuestro cuerpo puede funcionar correctamente, y si falla de repente la irrigación de un órgano, sus células mueren (infarto, necrosis, etc.).</p>
                    <p>Las arterias envejecen de forma especialmente rápida si están sometidas a lo que llamamos los factores de riesgo vascular: tabaco, hipertensión, colesterol y diabetes (los más importantes, pero hay más) dañan la estructura de la pared arterial dando lugar a quizás la enfermedad más grave de nuestra civilización: <strong>la arteriosclerosis.</strong></p>
                    <p>Las arterias aterosclerosas se ‘embozan', originando estenosis u oclusiones con el consiguiente déficit de aporte sanguíneo a los órganos que irrigan (infarto de miocardio en el corazón, ictus y hemiplejias si es el cerebro, isquemia y gangrena de extremidades, pudiendo llegar a la pérdida de extremidad, etc.). Pero también pueden dilatarse progresivamente originando aneurismas, que crecen lenta y sigilosamente, de forma asintomática, hasta llegar a romperse produciéndose entonces una hemorragia interna que suele ser fatal.</p>
                    <p>Nuestro trabajo como Angiólogos, Cirujanos Vasculares y Endovasculares es el diagnóstico y tratamiento de estas patologías una vez establecidas, pero lo más importante debe ser la <strong class="text-theme-color">prevención</strong>. El diagnóstico en fases precoces de la enfermedad puede evitar su progresión.</p>
                    <p>Una exploración vascular y un Eco-Doppler suelen ser suficientes para detectar la enfermedad, tratarla a tiempo y evitar graves complicaciones. Esto debería ser un consejo obligado en pacientes de cualquier edad con varices de todo tipo y especialmente en pacientes de edad (mayores de 55 años) que hayan fumado y/o presenten hipertensión y/o colesterol y/o diabetes.</p>
                    <p>Finalmente, recordar que la Angiología, Cirugía Vascular y Endovascular es una especialidad médico-quirúrgica, por lo que los tratamientos de que disponemos son muy extensos. La medicación es un arma básica contra los factores de riesgo vascular. Disponemos de múltiples tratamientos complementarios como son la esclerosis de varículas, presoterapia y drenaje linfático, láser vascular, etc.</p>
                    <p>Y por último, la cirugía, que puede ir desde una pequeña intervención de microcirugía estética de varices, con anestesia local y sin ingreso, pasando por técnicas mínimamente invasivas (cirugía endovascular) hasta intervenciones quirúrgicas de elevada complejidad técnica y alto riesgo, aunque con buenos resultados, pues son técnicas habituales, que de cualquier forma preferimos prevenir.</p>

